public class GetSiFangDiEntity {


    /**
     * sfd : 司放地
     * jd : 经 度
     * wd : 纬度
     */

    private String sfd;
    private String jd;
    private String wd;

    public String getSfd() {
        return sfd;
    }

    public void setSfd(String sfd) {
        this.sfd = sfd;
    }

    public String getJd() {
        return jd;
    }

    public void setJd(String jd) {
        this.jd = jd;
    }

    public String getWd() {
        return wd;
    }

    public void setWd(String wd) {
        this.wd = wd;
    }
}
